uint16 CapcomPC98Player::_flags = 0;

CapcomPC98Player::CapcomPC98Player(bool playerPrio, CapcomPC98Player::Flags playerFlag) : _playerPrio(playerPrio), _playerFlag(playerFlag), _curPos(nullptr), _numEventsTotal(0), _numEventsLeft(0), _volume(0), _midiTicker(0), _loop(false) {
	memset(_soundMarkers, 0, sizeof(_soundMarkers));
	_flags = 0;
}

void CapcomPC98Player::startSound(const uint8 *data, uint8 volume, bool loop) {
	stopSound();

	PC98AudioCore::MutexLock lock = lockMutex();
	_numEventsTotal = _numEventsLeft = READ_LE_UINT16(data);
	_data = _curPos = data + 2;
	_volume = volume;
	_loop = loop;

	_flags &= ~(stopFlag() | kFadeOut);
	_flags |= playFlag();
}

void CapcomPC98Player::stopSound() {
	while (_flags & playFlag()) {
		g_system->delayMillis(5);
		PC98AudioCore::MutexLock lock = lockMutex();
		_flags |= stopFlag();
	}
}

void CapcomPC98Player::nextTick() {
	if (_flags & playFlag()) {
		if (_flags & stopFlag()) {
			for (int i = 0; i < 16; ++i)
				send(0x007BB0 + i);
			_flags &= ~playFlag();

		} else if (_numEventsLeft) {
			bool eot = false;

			while (_numEventsLeft && !eot) {
				eot = false;

				uint32 in = READ_LE_UINT32(_curPos);
				if ((in & 0xFF) > _midiTicker)
					break;

				_midiTicker -= (in & 0xFF);
				if (_playerPrio || !(_flags & kPrioPlay))
					send(in >> 8);
				_curPos += 4;
				eot = !--_numEventsLeft;
			}

			if (eot) {
				if (_loop) {
					_numEventsLeft = _numEventsTotal;
					_curPos = _data;
				} else if (_playerPrio || !(_flags & kPrioPlay)) {
					for (int i = 0; i < 16; ++i)
						send(0x007BB0 + i);
					_flags &= ~playFlag();
				}
			}
		}
	}

	_midiTicker++;
}

CapcomPC98_MIDI::CapcomPC98_MIDI(MidiDriver::DeviceHandle dev, bool isMT32, MutexProc &mutexProc) : CapcomPC98Player(true, kStdPlay), _isMT32(isMT32), _mproc(mutexProc), _midi(nullptr), _programMapping(nullptr) {
	_midi = MidiDriver::createMidi(dev);
	uint8 *map = new uint8[128];
	assert(map);

	if (isMT32) {		
		memcpy(map, _programMapping_mt32ToGM, 128);
	} else {
		for (uint8 i = 0; i < 128; ++i)
			map[i] = i;
	}

	_programMapping = map;
}

CapcomPC98_MIDI::~CapcomPC98_MIDI() {
	_midi->close();
	delete _midi;
	delete[] _programMapping;
}

bool CapcomPC98_MIDI::init() {
	if (!_midi || !_programMapping)
		return false;

	if (_midi->open())
		return false;

	if (_isMT32) {
		_midi->sendMT32Reset();
	} else {
		static const byte gmResetSysEx[] = { 0x7E, 0x7F, 0x09, 0x01 };
		_midi->sysEx(gmResetSysEx, sizeof(gmResetSysEx));
		g_system->delayMillis(100);
	}

	reset();

	return true;
}

